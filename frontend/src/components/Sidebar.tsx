export default function Sidebar() {
  const [userRole, setUserRole] = useState<UserRole | null>(null);
  const location = useLocation();

  useEffect(() => {
    let cancelled = false;
    async function loadMe() {
      try {
        const me = await getMe();
        if (!cancelled) {
          setUserRole(me.role);
        }
      } catch {
        if (!cancelled) {
          setUserRole(null);
        }
      }
    }
    loadMe();
    return () => {
      cancelled = true;
    };
  }, []);

  const getNavItems = (): NavItem[] => {
    switch (userRole) {
      case "admin":
        return adminNavItems;
      case "teacher":
        return teacherNavItems;
      case "student":
        return studentNavItems;
      default:
        return [];
    }
  };

  const navItems = getNavItems();

  return (
    <aside className="w-56 flex-shrink-0 bg-white border-r border-[#d4cfe6] min-h-screen">
      <nav className="p-4 space-y-1 sticky top-0">
        {navItems.map((item) => {
          const isActive = location.pathname === item.path || location.pathname.startsWith(`${item.path}/`);
          return (
            <Link
              key={item.path}
              to={item.path}
              className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition ${
                isActive
                  ? "bg-[#372579] text-white"
                  : "text-gray-700 hover:bg-[#f5f3fa] hover:text-[#372579]"
              }`}
            >
              <span className={isActive ? "text-white" : "text-gray-500"}>{item.icon}</span>
              {item.label}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}
